// parseTargets 解析目标
func parseTargets(target string) []string {
	var targets []string
	lines := strings.Split(target, "\n")
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		// 支持CIDR格式
		if strings.Contains(line, "/") {
			// 检查是否是有效的CIDR（必须是IP地址格式）
			if _, _, err := net.ParseCIDR(line); err == nil {
				ips := expandCIDR(line)
				targets = append(targets, ips...)
				continue
			}
		}
		// 支持IP范围格式（如 192.168.1.1-192.168.1.100）
		// 只有当格式为 IP-IP 时才解析为范围，避免误解析域名中的连字符
		if strings.Contains(line, "-") && !strings.Contains(line, ".com") && !strings.Contains(line, ".net") && !strings.Contains(line, ".org") && !strings.Contains(line, ".cn") && !strings.Contains(line, ".io") {
			parts := strings.Split(line, "-")
			if len(parts) == 2 {
				startIP := net.ParseIP(strings.TrimSpace(parts[0]))
				// 只有当起始部分是有效IP时才当作IP范围处理
				if startIP != nil {
					ips := expandIPRange(line)
					if len(ips) > 0 {
						targets = append(targets, ips...)
						continue
					}
				}
			}
		}
		// 其他情况直接作为目标（域名、IP等）
		targets = append(targets, line)
	}
	return targets
}

// parsePorts 解析端口
func parsePorts(portStr string) []int {
	var ports []int
	portStr = strings.TrimSpace(portStr)

	// 预定义端口集
	if portStr == "top100" {
		return GetTop100Ports()
	}
	if portStr == "top1000" {
		return GetTop1000Ports()
	}

	parts := strings.Split(portStr, ",")
	for _, part := range parts {
		part = strings.TrimSpace(part)
		if strings.Contains(part, "-") {
			// 端口范围
			rangeParts := strings.Split(part, "-")
			if len(rangeParts) == 2 {
				start, _ := strconv.Atoi(rangeParts[0])
				end, _ := strconv.Atoi(rangeParts[1])
				for p := start; p <= end; p++ {
					ports = append(ports, p)
				}
			}
		} else {
			// 单个端口
			if p, err := strconv.Atoi(part); err == nil {
				ports = append(ports, p)
			}
		}
	}
	return ports
}

// getCategory 获取目标类型
func getCategory(target string) string {
	ip := net.ParseIP(target)
	if ip == nil {
		return "domain"
	}
	if ip.To4() != nil {
		return "ipv4"
	}
	return "ipv6"
}

// expandCIDR 展开CIDR
func expandCIDR(cidr string) []string {
	var ips []string
	_, ipnet, err := net.ParseCIDR(cidr)
	if err != nil {
		return ips
	}

	for ip := ipnet.IP.Mask(ipnet.Mask); ipnet.Contains(ip); incIP(ip) {
		ips = append(ips, ip.String())
	}

	// 移除网络地址和广播地址
	if len(ips) > 2 {
		ips = ips[1 : len(ips)-1]
	}
	return ips
}

// incIP IP自增
func incIP(ip net.IP) {
	for j := len(ip) - 1; j >= 0; j-- {
		ip[j]++
		if ip[j] > 0 {
			break
		}
	}
}

// expandIPRange 展开IP范围
func expandIPRange(ipRange string) []string {
	var ips []string
	parts := strings.Split(ipRange, "-")
	if len(parts) != 2 {
		return ips
	}

	startIP := net.ParseIP(strings.TrimSpace(parts[0]))
	endIP := net.ParseIP(strings.TrimSpace(parts[1]))
	if startIP == nil || endIP == nil {
		return ips
	}

	for ip := startIP; !ip.Equal(endIP); incIP(ip) {
		ips = append(ips, ip.String())
	}
	ips = append(ips, endIP.String())

	return ips
}

